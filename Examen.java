
public class Examen {

    private int idExamen;
    private float note;

    public Examen() {
    }

    public Examen(int idExamen, float note) {
        this.idExamen = idExamen;
        this.note = note;
    }

    public int getIdExamen() {
        return idExamen;
    }

    public void setIdExamen(int idExamen) {
        this.idExamen = idExamen;
    }

    public float getNote() {
        return note;
    }

    public void setNote(float note) {
        this.note = note;
    }
    
    
}
